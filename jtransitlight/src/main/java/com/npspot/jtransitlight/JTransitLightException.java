package com.npspot.jtransitlight;

public class JTransitLightException extends Exception
{
	public JTransitLightException(String message)
	{
		super(message);
	}
	public JTransitLightException(Throwable throwable)
	{
		super(throwable);
	}
	public JTransitLightException(String message, Throwable throwable)
	{
		super(message, throwable);
	}
}
